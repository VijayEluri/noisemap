package xifopen.noisemap.client.android;

public class AreaOfInterest {
	private String height, width, x, y, noise;
	public AreaOfInterest(String height, String width, String x, String y, String noise){
		this.height = height;
		this.width = width;
		this.x = x;
		this.y = y;
		this.noise = noise;
	}
}
